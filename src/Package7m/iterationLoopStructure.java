package Package7m;

public class iterationLoopStructure {
    public static void main(String[] args) {
        System.out.println("Get on the bicycle");
        for(int i=0; i<1000;i++){
            System.out.print("Pedalling");
        }
        System.out.println();
        System.out.println("Hit the brakes");
        System.out.println("Get off the bicycle");
    }
}
