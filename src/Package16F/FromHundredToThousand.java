package Package16F;

public class FromHundredToThousand {
    public static void main(String[] args) {
        for(int i=1;i<10;i++){
            for(int j=1;j<10;j++){
                for(int k=0;k<10;k++){
                    System.out.println(""+i+j+k);
                }
            }
        }
    }
}
