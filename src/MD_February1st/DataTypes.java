package MD_February1st;

public class DataTypes {
    public static void main(String[] args){
        int numberOne=14;
        int numberTwo=3;
        System.out.println(numberOne+numberTwo);
        System.out.println(numberOne/numberTwo);

        float floatNumberOne = 14;
        float floatNumberTwo = 3;

        System.out.println(floatNumberOne+floatNumberTwo);
        System.out.println(floatNumberOne/floatNumberTwo);

        boolean myBool = true;

        System.out.println(myBool);

    }
}
