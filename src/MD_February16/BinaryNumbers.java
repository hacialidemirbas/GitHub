package MD_February16;

public class BinaryNumbers {
    public static void main(String[] args) {
        for(int i=0 ; i<2 ; i++){

            for(int j=0 ; j<=1 ; j++){

                for(int k=0 ; k<2 ; k++){

                    System.out.println(""+i+j+k);

                }
            }
        }
    }
}
