package Package15F;

public class doWhile {
    public static void main(String[] args) {
        int counter = 10;
        do{
            System.out.println("Counter : "+counter);
            counter++;
        }
        while(counter<=10);
        System.out.println("With while loop");
        int counter2=10;
        while(counter2<=10){
            System.out.println(counter2);
            counter2++;
        }
    }
}
