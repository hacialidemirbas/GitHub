public class Loops12345 {
    public static void main(String[] args) {
        int number=1;
        for(int  i=1; i<4;i++){
            for(int j=1;j<6;j++){
                System.out.format("%5s",number);
                number++;
            }
            System.out.println();


        }
    }
}
