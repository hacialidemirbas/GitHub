package Exercises;

public class Kahoot {
    public static void main(String[] args) {
        for(byte i=1; i<10;i++){
            System.out.print("abc".charAt(1));
        }
        System.out.println();
        for(byte i=4; i>=0;i--){
            System.out.print("abcde".charAt(i));
        }
        System.out.println();
        char a='A';
        while(a<'Z'){
            a+=1;
             }
        System.out.println(a);
        System.out.println();
        double d;
        int b=0;
        do{
            b+=2;
            d=b;
        }
        while (b<5);
        System.out.println(d);
    }
}
