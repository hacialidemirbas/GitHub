package Exercises;

public class ex9m {
    public static void main(String[] args) {
        System.out.println('A'/9);
        System.out.println('a'/3);
        System.out.println((double)(2.0/3));
        System.out.println((float)(2.0/3));
        System.out.println(1+1);//2
        System.out.println("1"+1);//"11"
        System.out.println(""+1+1);//"11"
        System.out.println(""+(1+1));//"2"
        System.out.println((char)(01000001));
        System.out.println((byte)(01000001));
        System.out.println((float)(int)(3+4/8.0));
    }
}
