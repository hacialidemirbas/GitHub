package Package2;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(7%2);
        System.out.println(9%5);
        int x=99;
        System.out.println(x);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        x=x+5;
        System.out.println(x);
    }
}
